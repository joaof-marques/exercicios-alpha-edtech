import numpy as np

# Criando um array de inteiros usando NumPy
array_inteiros = np.array([1, 2, 3, 4, 5])

# Imprimindo o array
print("Array de inteiros:", array_inteiros)

# Realizando operações no array
maximo = np.max(array_inteiros)
minimo = np.min(array_inteiros)
print(maximo)
print(minimo)
